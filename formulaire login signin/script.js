function signin() {
  const signin = document.getElementById("signin");
  signin.hidden = !signin.hidden;
  const login = document.getElementById("login");
  login.hidden = !login.hidden;
}

function login() {
  const login = document.getElementById("login");
  login.hidden = !login.hidden;
  const signin = document.getElementById("signin");
  signin.hidden = !signin.hidden;
}